package main.game;

public class Wand {
	
	private String name;
	private double power;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setPower(double power) {
		this.power = power;
	}
	
	public double getPower() {
		return this.power;
	}

}
